#include "libft.h"

char	*ft_strdup(const char	*s1)
{
	char	*new;
	int		len;
	int		i;

	len = ft_strlen(s1);
	new = (char *)ft_calloc((len + 1), sizeof(char));
	if (!new)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		new[i] = s1[i];
		i++;
	}
	return (new);
}
